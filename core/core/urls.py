from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from drf_spectacular.views import SpectacularAPIView, SpectacularRedocView, SpectacularSwaggerView
from django.shortcuts import redirect


admin.site.site_header = 'MyCoin Blockchain Administration'
admin.site.site_title = 'MyCoin Admin'


urlpatterns = [
    # Redirect to MyCoin frontend at localhost:3000
    path('', lambda request: redirect('http://localhost:3000/'), name='home'),
    path('admin/', admin.site.urls),
    
    # MyCoin Blockchain APIs
    path('api/blockchain/', include('blockchain.urls')),
    
    # API Documentation
    path('api/schema/', SpectacularAPIView.as_view(), name='schema'),
    path('api/schema/swagger-ui/',
         SpectacularSwaggerView.as_view(url_name='schema'), name='swagger-ui'),
    path('api/schema/redoc/',
         SpectacularRedocView.as_view(url_name='schema'), name='redoc'),
] + static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
